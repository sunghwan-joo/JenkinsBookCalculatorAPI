package Pranodayd.CalculatorAPI;

public class Calculator {
	public int Subtraction(int num1, int num2) {
		int Res = num1 - num2;
		return Res;
	}
	
	public int Muliplication(int num1, int num2) {
		int Res = num1 * num2;
		return Res;
	}
	
	public int Addition(int num1, int num2) {
		int Res = num1 + num2;
		return Res;
	}
}
